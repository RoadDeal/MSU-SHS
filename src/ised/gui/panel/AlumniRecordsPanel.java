    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try {
            selectedBatch = (Batch) batchComboBox.getSelectedItem();
            if (selectedBatch == null) {
                return;
            }
            String search = searchTextField.getText().toString();
            alumniList = alumniService.getAlumniList(selectedBatch.getBatchID(), search);
            displayAlumniList(alumniList);
            searchTextField.setText("");
        } catch (ExceptionHandler ex) {
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid search", "Search", JOptionPane.WARNING_MESSAGE);
        }
}//GEN-LAST:event_searchButtonActionPerformed

