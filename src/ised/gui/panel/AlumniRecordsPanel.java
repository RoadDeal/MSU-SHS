    public void displayAlumniList(List<Alumni> alumniList) {
        ComponentFormatter.clearTable(alumniTableModel);
        for (Alumni alumni : alumniList) {
            alumniTableModel.addRow(new Object[]{alumni.getStudent().getFullName()});
        }
    }

    public void displayAlumniList() throws ExceptionHandler {
        selectedBatch = (Batch) batchComboBox.getSelectedItem();
        if (selectedBatch == null) {
            ComponentFormatter.clearTable(alumniTableModel);
            return;
        }
        alumniList = alumniService.getAlumniList(selectedBatch.getBatchID());
        displayAlumniList(alumniList);
    }

    private void setBatchComboBox() throws ExceptionHandler {
        DefaultComboBoxModel batchComboBoxModel = (DefaultComboBoxModel) batchComboBox.getModel();
        batchComboBoxModel.removeAllElements();
        batchComboBoxModel.addElement(null);
        batchList = alumniService.getBatchList();
        for (Batch batch : batchList) {
            batchComboBoxModel.addElement(batch);
        }
    }

