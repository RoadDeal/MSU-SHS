    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try {
            int index = classTable.getSelectedRow();
            if (index >= 0) {
                Class section = classList.get(index);

                if (isAdd) {
                    adviserService.addAdvisee(section.getClassID(), faculty.getEmployeeID());
                } else {
                    adviserService.editAdvisee(section.getClassID(), advisee.getAdviserID());
                }
                success = true;
                JOptionPane.showMessageDialog(parentFrame, "Employee has successfully assigned to the section", title, JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(parentFrame, "No section selected", "Add advisee", JOptionPane.WARNING_MESSAGE);
            }
        } catch (ExceptionHandler ex) {
        }
}//GEN-LAST:event_saveButtonActionPerformed

