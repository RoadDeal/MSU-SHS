    private void displayAdviserList() throws ExceptionHandler {
        classList = classService.getClassList(currentSchoolYear.getSchoolYearID(), "Active");
        ComponentFormatter.clearTable(classTableModel);
        for (Class section : classList) {
            classTableModel.addRow(new Object[]{getYearLevel(section.getYearLevel()) + " - " + section.getSection().getSectionName(),
                        getAdviser(section) == null ? "No adviser assigned" : getAdviser(section).getEmployee().getFullName()});
        }
    }

    private void selectAdvisee() {
        if (advisee != null) {
            int classIndex = -1;
            title = "Edit Advisee";
            isAdd = false;
            for (Class section : classList) {
                if (advisee.getSection().getClassID() == section.getClassID()) {
                    classIndex = classList.indexOf(section);
                }
            }
            classTable.setRowSelectionInterval(classIndex, classIndex);
        }
    }

    private String getYearLevel(int yearLevel) {
        if (yearLevel == 1) {
            return "I";
        } else if (yearLevel == 2) {
            return "II";
        } else if (yearLevel == 3) {
            return "III";
        } else if (yearLevel == 4) {
            return "IV";
        }
        return "";
    }

    private Adviser getAdviser(Class section) throws ExceptionHandler {
        return adviserService.getAdviser(section.getClassID());
    }

