    private void displaySchoolYearInfo() {
        if (!isAdd) {
            schoolYearTextField.setText(selectedSchoolYear.toString());
            statusComboBox.setSelectedItem(selectedSchoolYear.getSchoolYearStatus());
            if (selectedSchoolYear.getSchoolYearStatus().equalsIgnoreCase("Inactive")) {
                statusComboBox.setEnabled(true);
                enrollmentStartCheckBox.setEnabled(false);
                enrollmentEndCheckBox.setEnabled(false);
                startOfClassesCheckBox.setEnabled(false);
                return;
            }
            if (selectedSchoolYear.getEnrollmentBegin() != null) {
                enrollmentStartCheckBox.setSelected(true);
                updateEnrollmentStartComboBox(true);
                ComponentFormatter.formatDateNoToWord(selectedSchoolYear.getEnrollmentBegin(), monthEnrollmentStartComboBox, dayEnrollmentStartComboBox);
            }
            if (selectedSchoolYear.getEnrollmentEnd() != null) {
                enrollmentEndCheckBox.setSelected(true);
                updateEnrollmentEndComboBox(true);
                ComponentFormatter.formatDateNoToWord(selectedSchoolYear.getEnrollmentEnd(), monthEnrollmentEndComboBox, dayEnrollmentEndComboBox);
            }
            if (selectedSchoolYear.getStartOfClasses() != null) {
                startOfClassesCheckBox.setSelected(true);
                updateStartOfClassesComboBox(true);
                ComponentFormatter.formatDateNoToWord(selectedSchoolYear.getStartOfClasses(), monthStartOfClassesComboBox, dayStartOfClassesComboBox);
            }
        } else {
            schoolYearTextField.setText(selectedSchoolYear.toString());
        }
    }

