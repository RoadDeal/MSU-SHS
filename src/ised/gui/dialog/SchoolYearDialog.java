    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            Date enrollBegin = null;
            Date enrollEnd = null;
            Date startOfClasses = null;

            if (enrollmentStartCheckBox.isSelected()) {
                enrollBegin = ComponentFormatter.formatDate(selectedSchoolYear.getYearFrom(), monthEnrollmentStartComboBox, dayEnrollmentStartComboBox);
            }
            if (enrollmentEndCheckBox.isSelected()) {
                enrollEnd = ComponentFormatter.formatDate(selectedSchoolYear.getYearFrom(), monthEnrollmentEndComboBox, dayEnrollmentEndComboBox);
            }
            if (startOfClassesCheckBox.isSelected()) {
                startOfClasses = ComponentFormatter.formatDate(selectedSchoolYear.getYearFrom(), monthStartOfClassesComboBox, dayStartOfClassesComboBox);
            }

            if (enrollmentStartCheckBox.isSelected() && enrollmentEndCheckBox.isSelected()) {
                if (enrollBegin.getMonth() + 1 > enrollEnd.getMonth() + 1 || (enrollBegin.getMonth() + 1 == enrollEnd.getMonth() + 1 && enrollBegin.getDay() > enrollEnd.getDay())) {
                    JOptionPane.showMessageDialog(parentFrame, "Invalid enrollment period", "WARNING", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            if (startOfClassesCheckBox.isSelected() && enrollmentEndCheckBox.isSelected()) {
                if (startOfClasses.before(enrollEnd)) {
                    JOptionPane.showMessageDialog(parentFrame, "Start of classes must be before the end of enrollment", "WARNING", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }


            if (isAdd) {
                SchoolYear schoolYear = new SchoolYear();
                schoolYear.setYearFrom(selectedSchoolYear.getYearFrom());
                schoolYear.setYearTo(selectedSchoolYear.getYearTo());
                schoolYear.setEnrollmentBegin(enrollBegin);
                schoolYear.setEnrollmentEnd(enrollEnd);
                schoolYear.setStartOfClasses(startOfClasses);
                schoolYear.setSchoolYearStatus(statusComboBox.getSelectedItem().toString());
                schoolYearService.addSchoolYear(schoolYear);
                success = true;
                JOptionPane.showMessageDialog(this, "School Year is successfully added", "Add School Year", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            } else {
                selectedSchoolYear.setEnrollmentBegin(enrollBegin);
                selectedSchoolYear.setEnrollmentEnd(enrollEnd);
                selectedSchoolYear.setStartOfClasses(startOfClasses);
                selectedSchoolYear.setSchoolYearStatus(statusComboBox.getSelectedItem().toString());
                schoolYearService.editSchoolYear(selectedSchoolYear);
                success = true;
                JOptionPane.showMessageDialog(this, "School Year is successfully edited", "Edit School Year", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            }
        } catch (ExceptionHandler ex) {
            Logger.getLogger(SchoolYearDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_saveButtonActionPerformed

