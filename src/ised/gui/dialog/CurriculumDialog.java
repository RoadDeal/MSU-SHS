    private void displayCurriculumInfo() {
        if (curriculum != null) {
            isAdd = false;
            curriculumYearComboBox.addItem(curriculum.getCurriculumYear());
            curriculumYearComboBox.setSelectedItem(curriculum.getCurriculumYear());
            curriculumYearComboBox.setEnabled(false);
            if (curriculum.getStatus().equalsIgnoreCase("Inactive")) {
                statusComboBox.setSelectedItem(curriculum.getStatus());
                statusComboBox.setEnabled(true);
            }
        } else {
            setCurriculumYearComboBox();
        }
    }

    public void setCurriculumYearComboBox() {
        curriculumYearComboBox.removeAllItems();
        Calendar cal = Calendar.getInstance();
        int currentYear = cal.get(Calendar.YEAR);
        for (int i = currentYear; i <= (currentYear + 5); i++) {
            curriculumYearComboBox.addItem(i);
        }
        curriculumYearComboBox.setSelectedItem(currentYear);
    }

