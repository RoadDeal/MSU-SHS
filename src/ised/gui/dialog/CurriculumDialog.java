    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if (isAdd) {
                int curriculumYear = Integer.parseInt(curriculumYearComboBox.getSelectedItem().toString());
                if (!curriculumService.checkCurriculum(curriculumYear)) {
                    curriculumService.addCurriculum(curriculumYear);
                    curriculum = curriculumService.getCurriculumByYear(curriculumYear);
                    JOptionPane.showMessageDialog(this, "Curriculum successfully added", "Add Curriculum", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, curriculumYear + " Curriculum already Exist", "Add Curriculum", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                String status = statusComboBox.getSelectedItem() + "";
                curriculumService.editCurriculum(curriculum.getCurriculumID(), status);
                JOptionPane.showMessageDialog(this, "Curriculum successfully edited", "Edit Curriculum", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            }

        } catch (ExceptionHandler ex) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

